drop view if exists public.orders_dashboard;
create view public.orders_dashboard
with (security_invoker = true) as
select
  o.id,
  o.order_number,
  o.customer_name,
  o.phone,
  o.city,
  o.notes,
  o.items_json,
  o.total,
  o.status,
  o.source,
  o.created_at,
  o.user_id,
  o.customer_email,
  o.delivery_lat,
  o.delivery_lng,
  o.delivery_maps_link,
  o.delivery_location_source,
  coalesce(jsonb_array_length(o.items_json), 0) as items_count,
  coalesce(jsonb_array_length(o.items_json), 0) as line_items_count
from public.orders o;

create or replace function public.handle_new_user_profile()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email, full_name)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data ->> 'full_name', new.raw_user_meta_data ->> 'name')
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created_create_profile on auth.users;
create trigger on_auth_user_created_create_profile
after insert on auth.users
for each row execute function public.handle_new_user_profile();
