create table if not exists public.product_variants (
  id bigint generated by default as identity primary key,
  product_id bigint not null references public.products(id) on delete cascade,
  size_label text not null,
  color_name text not null,
  color_hex text,
  price numeric not null check (price >= 0),
  compare_price numeric,
  stock_quantity integer not null default 0 check (stock_quantity >= 0),
  sku text,
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.product_images (
  id bigint generated by default as identity primary key,
  product_id bigint not null references public.products(id) on delete cascade,
  color_name text,
  image_path text not null,
  alt_text text,
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.orders (
  id bigint generated always as identity primary key,
  created_at timestamptz not null default now(),
  customer_name text,
  phone text,
  city text,
  notes text,
  items_json jsonb,
  total numeric,
  status text default 'pending' check (status is null or status in ('pending', 'confirmed', 'processing', 'delivered', 'cancelled')),
  source text default 'website',
  order_number text not null unique,
  user_id uuid references auth.users(id) on delete set null,
  customer_email text,
  delivery_lat double precision,
  delivery_lng double precision,
  delivery_maps_link text,
  delivery_location_source text
);

create index if not exists idx_products_active_sort on public.products (is_active, sort_order, id);
